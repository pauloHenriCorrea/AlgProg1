"""
Escreva um programa que receba um número inteiro a e verifique se a é positivo,
se a é negativo ou se a é igual a 0. Faça a simulação passo a passo da execução
de sua solução.
"""

numero = int(input("Informe um número inteiro: "))

if numero > 0:
    print("Positivo")
elif numero < 0:
    print("Negativo")
else:
    print("Nulo")
