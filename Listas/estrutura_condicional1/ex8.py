# Entrada de dados
num = float(input("Informe um número: "))

# Computação/Saída
if num == 5:
    print("Igual a 5")
elif num == 200:
    print("Igual a 200")
elif num == 400:
    print("Igual a 400")
elif num >= 500 and num <= 1000:
    print("Está no intervalo entre 500 e 1000")
else:
    print("Está dora dos escopos anteriores")
